export const getKeyCodeName = (keyCode: number) => {
  const entries = Object.entries(keys)
  const found = entries.find(x => x[1] === keyCode)
  return found ? found[0] : null
}

export const arrayOfKeycCodesToHotkeyStr = (arrayOfKeyCodes: (string | number)[]) =>
  `[${arrayOfKeyCodes.map(x => getKeyCodeName(parseInt(x as string, 10))).join(' - ')}]`
